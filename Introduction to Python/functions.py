# Create a program that calculates total sales earned in a year for a pizza company
# Implement a function that takes in two inputs: Quantity Sold and Price per Unit
# Return the total sales for that year

total_sales = 0
quantity = 200_000 # measured in units
price = 12.50   # $12.50 per unit

def compute_annual_sales(quantity_sold, price_per_unit):
  total_sales = quantity_sold * price_per_unit
  return total_sales

print("Total sales this year are $" + str(compute_annual_sales(quantity, price)))

