# Create a function that takes a list of integers and multiples each item in the list by 2
# Return the updated list values and print it to the screen

list_of_integers_1 = [1,2,3,4,5]

def multiplyByTwo(numbers):
  for index in range(len(numbers)):
    numbers[index] = numbers[index] * 2
  return numbers

print(multiplyByTwo(list_of_integers_1))
print(list_of_integers_1) # NOTE: The previous list gets updated too because the original list and the modified list reference the original list

# NOTE: Using copy() solves above problem with the original list changing since your copy refers to a new list
list_of_integers_2 = [6,7,8,9,10]
updated_list = list_of_integers_2.copy()

print(multiplyByTwo(updated_list))
print(list_of_integers_2)